function getPosToRightOfPos(pos, angle, distance) {
	let x = (pos.x+((Math.cos((angle)+(Math.PI/4)))*distance));
	let y = (pos.y+((Math.sin((angle)+(Math.PI/4)))*distance));
	
	let rightPos = new Vec3(x, y, pos.z);
	
	return rightPos;
}

