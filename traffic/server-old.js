let spawnCivilians = false;
let spawnTraffic = true;

let trafficVehicles = [];
let trafficCivilians = [];
let maxVehiclesPerNode = 2;

// ----------------------------------------------------------------------------

addEventHandler("OnResourceStart", function(event, resource) {
	if(spawnTraffic) {
		for(let i=0,i2=trafficNodes.length; i < i2-1; i++) {	
			for(let k=0,k2=maxVehiclesPerNode; k < k2-1; k++) {
				let tempNode = trafficNodes[i][k];
				let nextNode = trafficNodes[i][k+1];
				let tempPosition = new Vec3(tempNode[0], tempNode[1], tempNode[2] + 1);
				let tempNextPosition = new Vec3(nextNode[0], nextNode[1], nextNode[2] + 1);
				let tempHeading = getHeadingFromPosToPos(tempPosition, tempNextPosition);
				let rightPos = getPosToRightOfPos(tempPosition, tempHeading, 2);
				
				if(!isAnotherVehicleInProximity(rightPos)) {
					let vehicleModel = getRandomTrafficCarModel();
					let tempVehicle = createVehicle(vehicleModel, rightPos);
					tempVehicle.game = GAME_GTA_III;
					tempVehicle.position = rightPos;
					tempVehicle.heading = tempHeading;
					//tempVehicle.setData("AI", true, true);
					addToWorld(tempVehicle);
					
					let tempDriver = createCivilian(getPedForVehicleModel(vehicleModel), tempPosition);
					tempDriver.game = GAME_GTA_III;
					tempDriver.position = tempPosition;
					//tempDriver.setData("AI", true, true);
					tempDriver.setData("AI.V", tempVehicle.id, true);
					addToWorld(tempDriver);	
				
					trafficVehicles.push(tempVehicle);
					trafficCivilians.push(tempDriver);
				}
			}
		}
	}
});

